package com.utility.nls;

public class LanguagePresenter {

	private String usEnglish;
	private String canadianFrench;
	private String french;
	private String indonasian;
	private String simplifiedChinese;
	private String traditionalChinese;
	private String maxicanSpenish;
	private String vietnamese;
	private String polish;

	public LanguagePresenter(String usEnglish, String canadianFrench, String french, String indonasian, String simplifiedChinese,
			String traditionalChinese, String maxicanSpenish, String vietnamese, String polish) {
		super();
		this.usEnglish = usEnglish;
		this.canadianFrench = canadianFrench;
		this.french = french;
		this.indonasian = indonasian;
		this.simplifiedChinese = simplifiedChinese;
		this.traditionalChinese = traditionalChinese;
		this.maxicanSpenish = maxicanSpenish;
		this.vietnamese = vietnamese;
		this.polish = polish;
	}

	public String getUsEnglish() {
		return usEnglish;
	}

	public void setUsEnglish(String usEnglish) {
		this.usEnglish = usEnglish;
	}

	public String getCanadianFrench() {
		return canadianFrench;
	}

	public void setCanadianFrench(String canadianFrench) {
		this.canadianFrench = canadianFrench;
	}

	public String getFrench() {
		return french;
	}

	public void setFrench(String french) {
		this.french = french;
	}

	public String getIndonasian() {
		return indonasian;
	}

	public void setIndonasian(String indonasian) {
		this.indonasian = indonasian;
	}

	public String getSimplifiedChinese() {
		return simplifiedChinese;
	}

	public void setSimplifiedChinese(String simplifiedChinese) {
		this.simplifiedChinese = simplifiedChinese;
	}

	public String getTraditionalChinese() {
		return traditionalChinese;
	}

	public void setTraditionalChinese(String traditionalChinese) {
		this.traditionalChinese = traditionalChinese;
	}

	public String getMaxicanSpenish() {
		return maxicanSpenish;
	}

	public void setMaxicanSpenish(String maxicanSpenish) {
		this.maxicanSpenish = maxicanSpenish;
	}

	public String getVietnamese() {
		return vietnamese;
	}

	public void setVietnamese(String vietnamese) {
		this.vietnamese = vietnamese;
	}

	public String getPolish() {
		return polish;
	}

	public void setPolish(String polish) {
		this.polish = polish;
	}
}
